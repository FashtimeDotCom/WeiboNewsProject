package cn.ssm.po;

public class EntityMo {
	private String entity;
	private String pastiveMo;
	private String negativeMo;
	
	public EntityMo() {
		super();
	}
	public String getEntity() {
		return entity;
	}
	public void setEntity(String entity) {
		this.entity = entity;
	}
	public String getPastiveMo() {
		return pastiveMo;
	}
	public void setPastiveMo(String pastiveMo) {
		this.pastiveMo = pastiveMo;
	}
	public String getNegativeMo() {
		return negativeMo;
	}
	public void setNegativeMo(String negativeMo) {
		this.negativeMo = negativeMo;
	}
	
	
	

}
